with open ("weasyprint/text/ffi.py") as f:
    data = f.read()

body = data.split("ffi.cdef('''")[-1].split("''')")[0]
funcs = []
for line in body.split('\n'):
    if '(' in line:
        line = line.replace(' (', '(')
        pref = line.split('(')[0]
        words = pref.split(' ')
        funcs.append(words[-1])

print('// NO MANUAL EDITS. GENERATED BY YAMAKER PYPI from 02-cdefs.py')
print ('\n'.join([f'extern void* {func};' for func in funcs]))
print('extern void* every_needed_function_for_weasy_print[] = {')
print (',\n'.join([f'  (void*)(&{func})' for func in funcs]))
print('};')
